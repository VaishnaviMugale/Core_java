package Constructors;

public class Demo1 {

	void methodone() {
		System.out.println("no-args method");
	}
	void methodone(int i) {
		System.out.println("int-args method");
	}
	void methodone( double d) {
		System.out.println("double-args method");
		
	}
	void methodone(int i , double d) {
		System.out.println("int-args and double-args method");
	}
}
