package method;

public class MethodExample3 {
	static int Square(int x) {
		return x*x;
    	 
    	 
     }
	
	public static void main(String[]args) {
	    
		System.out.println(Square(5));
		System.out.println(Square(7));
		System.out.println(Square(25));
		System.out.println(Square(19));
		
		
	}
}
